package org.team1540.robot2026.subsystems.PieceDetection;

public class Centroid {
    private final double x;
    private final double y;

    public Centroid(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
